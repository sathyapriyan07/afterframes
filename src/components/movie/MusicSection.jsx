export function MusicSection({ links = [] }) {
  const ost = links.filter(l => l.type === 'OST')
  const bgm = links.filter(l => l.type === 'BGM')

  if (!links.length) return null

  const renderLinks = (items, label) => {
    if (!items.length) return null
    return (
      <div className="mb-6">
        <h3 className="text-sm font-medium text-dark-300 uppercase tracking-wider mb-3">{label}</h3>
        <div className="flex flex-wrap gap-2">
          {items.map((link) => {
            const platform = PLATFORMS[link.platform] || {
              label: link.platform,
              color: 'bg-dark-600 hover:bg-dark-500',
              icon: () => <Music size={16} />,
            }
            const Icon = platform.icon
            return (
              <a
                key={link.id}
                href={link.url}
                target="_blank"
                rel="noopener noreferrer"
                className={`inline-flex items-center gap-2 px-4 py-2 rounded-xl text-white text-sm font-medium transition-all duration-200 active:scale-95 ${platform.color}`}
              >
                <Icon />
                {platform.label}
                <ExternalLink size={12} className="opacity-70" />
              </a>
            )
          })}
        </div>
      </div>
    )
  }

  return (
    <div className="glass-dark rounded-2xl p-5">
      <h2 className="section-title">Music</h2>
      {renderLinks(ost, 'Original Soundtrack (OST)')}
      {renderLinks(bgm, 'Background Score (BGM)')}
    </div>
  )
}
